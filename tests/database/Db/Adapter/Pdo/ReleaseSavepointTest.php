declare(strict_types=1);

namespace Phalcon\Tests\Database\Db\Adapter\Pdo;

use Phalcon\Tests\AbstractDatabaseTestCase;
use Phalcon\Tests\Support\Traits\DiTrait;

final class ReleaseSavepointTest extends AbstractDatabaseTestCase
{
    use DiTrait;

    public function setUp(): void
    {
        $this->setNewFactoryDefault();
        $this->setDatabase();
    }

    /**
     * Tests Phalcon\Db\Adapter\Pdo :: releaseSavepoint()
     *
     * @author Phalcon Team <team@phalcon.io>
     * @since  2026-05-18
     *
     * @group mysql
     * @group pgsql
     * @group sqlite
     */
    public function testDbAdapterPdoReleaseSavepoint(): void
    {
        $db = $this->container->get('db');

        $db->setNestedTransactionsWithSavepoints(true);
        $this->assertTrue($db->begin());
        $this->assertTrue($db->createSavepoint('sp1'));
        $this->assertTrue($db->releaseSavepoint('sp1'));
        $this->assertTrue($db->rollback());
    }
}
