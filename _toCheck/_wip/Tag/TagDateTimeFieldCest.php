     *
     * @author Nikolaos Dimopoulos <nikos@phalconphp.com>
     * @since  2014-09-05
     */
    public function testDateTimeFieldStringParameter(UnitTester $I)
    {
        $options  = 'x_name';
        $expected = '<input type="datetime" id="x_name" name="x_name"';

        $this->testFieldParameter(
            $I,
            'dateTimeField',
            $options,
            $expected,
            false
        );

        $options  = 'x_name';
        $expected = '<input type="datetime" id="x_name" name="x_name"';

        $this->testFieldParameter(
            $I,
            'dateTimeField',
            $options,
            $expected,
            true
        );
    }

